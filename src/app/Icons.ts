function iconClassToText(x: string): string
{
  return x.substring(4)
    .replace(/^./, (y) => y.toUpperCase())
    .replace(/-./g, (z) => ' ' + z.substring(1).toUpperCase())
  ;
}

export const Icons: ListOptions<string> = IconClasses
  .map((className) => ({
    text: iconClassToText(className),
    value: className,
  }))
;

Icons.sort((a, b) => a.text.localeCompare(b.text));
